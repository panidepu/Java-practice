package apples;

public class switchs {
	public static void main(String args[]){
		int age = 6;
		switch(age){
		case 1 :
			System.out.println("u can crawl");
			break;
		case 2 :
			System.out.println("you can walk");
			break;
		case 3 :
			System.out.println("you can talk");
			break;
		default :
			System.out.println("null");
			break;
	}
	}

}
